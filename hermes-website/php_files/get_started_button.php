	<h2>Hermes!</h2>
	<p>Hermes is an online distributed chat system that enables users to sign up, search for their friends and initiate a chat with them.</p>
<p>
Our aim is to provide users with an excellent experience and help them stay in touch with their relatives and friends by enabling them to initiate chats and exchange messages, ensuring their requests are always processed efficiently, at the shortest time possible, and without any noticable delays that may be inconvenient. Our aim is to also protect users' privacy and confidentiality.
		</p>
<p>Do not have an account? that's not a problem, you can create one by clicking here. You will be asked to provide some personal information such as name, username, email and you will be required to set a password. Once you have signed up, you can login using the email you have provided during registration and the password you have created. Once logged-in, you will be redirected to your home page in which you will be able to search for other users registered on the system, initiate a chat with them, send and receive messages, search initiated chats, delete messages or/and chats should you wish to do so.
		</p>


